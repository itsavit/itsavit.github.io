/* Countdownd timer */

$(function(){

  var x = setInterval(function() {
    var gameCountdownDate = new Date("Feb 1, 2018 16:30:00").getTime();
    var now = new Date().getTime();
    var distance = gameCountdownDate - now;
    var days = Math.floor(distance / (1000 * 60 * 60 * 24));
    var hours = Math.floor((distance % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
    var minutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
    var seconds = Math.floor((distance % (1000 * 60)) / 1000);
    document.getElementById("game-countdown").innerHTML = days + "d " + hours + "h " + minutes + "m " + seconds + "s ";document.getElementById("countdown2").innerHTML = days + "d " + hours + "h " + minutes + "m " + seconds + "s ";
    if (distance < 0) {
      clearInterval(x);
      document.getElementById("game-countdown").innerHTML = "Event Completed";
    }
  }, 1000);
});